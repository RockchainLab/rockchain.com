			<h6 class="question-wrapper__question">How does a Token Sale work?</h6>
			<div class="question-wrapper__answer">
				<p>Token Sales (or Initial Coin Offerings, ICOs) have become a key way to fund early stage projects that typically have a use-case based on the blockchain or other distributed ledger technology. It is a crowdfunding event (typically  a one-off or also with further sales planned) in which a new cryptocurrency asset - a Token or Coin - is sold to supporters of the project, early adopters and enthusiasts in exchange for money or value today. The fundraise is normally in Bitcoin (BTC) or Ether (ETH) however it may also depend on which blockchain the sale is supported by (NEO, Stellar, Waves etc.) The funds raised are typically assigned to development and other operational costs related to the functions, products and services described in the whitepaper and accompanying roadmap. Token Sales often involve both private sale and public sale phases. The Token Sale may specify   a minimum amount that the project  needs to raise in order to deliver it’s roadmap (or sometimes a specified portion of the roadmap at different funding levels) and/or there may be a ‘soft cap’ (but this term is not always used ) and/or a maximum amount of capital required (referred to as a hard cap) after which the  fundraising will close and excess funds will be returned to senders. Some Token Sales are uncapped.  Also relevant are token supplies and emission schedules and more. There are several permutations and we can advise you on these and what may suit your project best.</p>
