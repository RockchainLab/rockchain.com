			<h6 class="question-wrapper__question">What is the difference between a Utility Token and Security Token?</h6>
			<div class="question-wrapper__answer">
				<p>This is a very important question and the design and legal classification of your token is critical to your project. Security tokens are designed to be or act like the company’s share and may have economic rights and benefits attached (share of profits etc.) or otherwise participate in the financial participation of the issuer;  while utility tokens represent access to company’s product or service and have solely a practical use (access, value exchange, currency, governance, gas or other function). Utility tokens are typically exempted from regulation and security laws. Tokens that are deemed securities will have to comply with the applicable laws based on where the token is issued and to whom it is marketed or offered. This requires regulatory analysis early in the design of your Token Sale and because it varies between jurisdictions, this may affect where you can sell your Token or allow it to be made available.  The consequences of non-compliance can be severe. Many tokens are hybrids and many token sales to date have been non-compliant.</p>
